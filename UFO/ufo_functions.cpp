void end_game(std::string answer, std::string codeword) {
    if (answer == codeword) {
        std::cout << "\nHooray! You saved the person and earned a medal of honor!\n";
    } else {
        std::cout << "\nOh no! The UFO just flew away with another person!\n";
    }
}

void display_status(std::vector<char> incorrect, std::string answer) {
    // Display all of the incorrect guesses made so far
    std::cout << "Incorrect Guesses: ";
    for (int i = 0; i < incorrect.size(); i++) {
        std::cout << incorrect[i] << " ";
    }

    // Display the answer so far
    std::cout << "\nCodeword: ";
    for (int i = 0; i < answer.length(); i++) {
        std::cout << answer[i] << " ";
    }
    std::cout << "\n";
}

bool already_guessed(std::vector<char> incorrect, std::string answer, char letter) {
  // Check if letter is a previous incorrect guess
  for (int i = 0; i < incorrect.size(); i++) {
    if (letter == incorrect[i]) {
      std::cout << "You already guessed this letter.\n";
      return true;
    }
  }
  // Check if letter is a previous correct guess
  for (int i = 0; i < answer.length(); i++) {
    if (letter == answer[i]) {
      std::cout << "You already guessed this letter.\n";
      return true;
    }
  }
  return false;
}

